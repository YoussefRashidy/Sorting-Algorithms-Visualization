    // for visualization mode until implementing asymptotic functions plotting
    private final Color BAR_COLOR = Color.web("#38bdf8");  // soft cyan

    private Pane animatedBars(double width, double height) {
        Pane pane = new Pane();
        int numBars = 50;
        double gap = 4;
        double barW = width / numBars - gap;
        Random rng = new Random();
        for (int i = 0; i < numBars; i++) {
            Rectangle bar = new Rectangle();
            double x = (barW + gap) * i + gap / 2;
            bar.setX(x);
            bar.setWidth(barW);

            double startHeight = 50 + rng.nextDouble() * 80;
            double maxHeight = rng.nextDouble() * height;

            bar.setHeight(startHeight);
            bar.setY(height - startHeight);

            bar.setArcWidth(12);
            bar.setArcHeight(12);

            bar.setFill(BAR_COLOR);

            // Animation for bars
            Timeline tl = new Timeline();
            double dur = 1.5 + rng.nextDouble() * 2.5;
            KeyValue kvh1 = new KeyValue(bar.heightProperty(), startHeight, Interpolator.EASE_BOTH);
            KeyValue kvh2 = new KeyValue(bar.heightProperty(), maxHeight, Interpolator.EASE_BOTH);
            KeyValue kvy1 = new KeyValue(bar.yProperty(), height - startHeight, Interpolator.EASE_BOTH);
            KeyValue kvy2 = new KeyValue(bar.yProperty(), height - maxHeight, Interpolator.EASE_BOTH);

            KeyFrame frame1 = new KeyFrame(Duration.ZERO, kvh1, kvy1);
            KeyFrame frame2 = new KeyFrame(Duration.seconds(dur), kvh2, kvy2);
            tl.getKeyFrames().addAll(frame1, frame2);

            tl.setAutoReverse(true);
            tl.setCycleCount(Animation.INDEFINITE);
            tl.setDelay(Duration.seconds(3 * rng.nextDouble()));
            tl.play();
            animations.add(tl);
            pane.getChildren().add(bar);

        }
        return pane;
    }

    @Override
    public void play() {
        animations.forEach(Timeline::play);
    }

    @Override
    public void pause() {
        animations.forEach(Timeline::pause);
    }

    @Override
    public void stop() {
        animations.forEach(Timeline::stop);
    }

    public InputData collectData() {
        InputData data = new InputData();
        if (autoGenerated.isSelected()) {
            data.autoGeneration = true;
            String[] parts = arraySize.getText().split(",");
            for (String part : parts) {
                data.arraySizes.add(Integer.valueOf(part));
            }
            data.maxValue = Integer.parseInt(arrayMaxValue.getText());
            ArrayList<ArrayType> generationMode = new ArrayList<>();
            if (randomArray.isSelected()) generationMode.add(ArrayType.RANDOM);
            if (sortedArray.isSelected()) generationMode.add(ArrayType.SORTED);
            if (inverselySorted.isSelected()) generationMode.add(ArrayType.INVERSELY_SORTED);
            if (nearlySortedArray.isSelected()) generationMode.add(ArrayType.NEARLY_SORTED);
            data.generationMods = generationMode.toArray(new ArrayType[0]);
        } else {
            data.autoGeneration = false;
            data.files = new ArrayList<>(this.files);
        }
        for (CheckBox box : checkBoxes) {
            if (box.isSelected()) data.algorithms.add(box.getText());
        }
        return data;
    }

