    public void listenersWiring() {
        arraySize.textProperty().addListener((obs, oldVal, newVal) -> {
            if (!newVal.matches("[0-9,]*")) {
                arraySize.setText(newVal.replaceAll("[^0-9,]", ""));
            }
        });

        // Allow only one max for now consider adding multiple max values
        arrayMaxValue.textProperty().addListener((obs, oldVal, newVal) -> {
            if (!newVal.matches("[0-9]*")) {
                arrayMaxValue.setText(newVal.replaceAll("[^0-9]", ""));
            }
            if (!arrayMaxValue.getText().isEmpty())
                try {
                    Integer.parseInt(arrayMaxValue.getText());
                } catch (NumberFormatException e) {
                    // Prevent the max from exceeding the int max value
                    arrayMaxValue.setText(String.valueOf(Integer.MAX_VALUE));
                }
        });

        BooleanBinding anySelected = randomArray.selectedProperty()
                .or(sortedArray.selectedProperty())
                .or(inverselySorted.selectedProperty())
                .or(nearlySortedArray.selectedProperty());

        BooleanBinding autoGenerationInvalid =
                arraySize.textProperty().isEmpty().or(anySelected.not());

        javafx.beans.binding.BooleanExpression anyAlgorithmSelected = checkBoxes.getFirst().selectedProperty();
        for (int i = 1; i < checkBoxes.size(); i++) {
            anyAlgorithmSelected = anyAlgorithmSelected.or(checkBoxes.get(i).selectedProperty());
        }
        BooleanBinding noAlgorithmSelected = anyAlgorithmSelected.not();

        BooleanBinding fileInvalid = new BooleanBinding() {
            {
                super.bind(chooseFileButton.focusedProperty());
            }

            @Override
            protected boolean computeValue() {
                return fileLoaded.isSelected() && (files == null || files.isEmpty());
            }
        };
        chooseFileButton.setOnAction(event -> {
            files = fileChooser.showOpenMultipleDialog(this.getScene().getWindow());
        });

        startButton.disableProperty().bind(
                noAlgorithmSelected
                        .or(autoGenerated.selectedProperty().and(autoGenerationInvalid))
                        .or(fileLoaded.selectedProperty().and(fileInvalid)));

        autoButton.setOnAction(e -> {
            controller.runAutoComparison();
        });

        startButton.setOnAction(event -> {
            InputData data = collectData();
            autoGenerated.setSelected(true);
            fileLoaded.setSelected(false);
            arraySize.clear();
            arrayMaxValue.clear();
            randomArray.setSelected(false);
            sortedArray.setSelected(false);
            inverselySorted.setSelected(false);
            nearlySortedArray.setSelected(false);
            checkBoxes.forEach(cb -> cb.setSelected(false));
            files = null;
            controller.setData(data);
            controller.setMode(mode);
            controller.processData();
        });

        resetButton.setOnAction(event -> {
            autoGenerated.setSelected(true);
            fileLoaded.setSelected(false);
            arraySize.clear();
            arrayMaxValue.clear();
            randomArray.setSelected(false);
            sortedArray.setSelected(false);
            inverselySorted.setSelected(false);
            nearlySortedArray.setSelected(false);
            checkBoxes.forEach(cb -> cb.setSelected(false));
            autoButton.setVisible(mode.equalsIgnoreCase("Comparison Mode"));
            autoButton.setManaged(mode.equalsIgnoreCase("Comparison Mode"));
            files = null;
        });


    }

